#!/usr/bin/env node
/**
 * Extract the message-type string constants from
 *   src/shared/popup-launcher-protocol.ts
 * and write a plain-JS mirror to
 *   popup/popup-launcher-protocol.js
 *
 * popup-launcher.js is shipped verbatim (not bundled by esbuild),
 * so it can't import .ts. We generate a tiny JS file at build time
 * that exposes the same constants on `window.OpenBiliClawLauncher`.
 *
 * This script is idempotent and safe to run on every build.
 */

import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const root = resolve(import.meta.dirname, "..");
const src = resolve(root, "src/shared/popup-launcher-protocol.ts");
const dst = resolve(root, "popup/popup-launcher-protocol.js");

const text = await readFile(src, "utf-8");

// Match: export const NAME = "value";
const re = /export const (\w+)\s*=\s*"([^"]+)"\s*;/g;
const entries = [];
for (const m of text.matchAll(re)) {
  entries.push([m[1], m[2]]);
}

if (entries.length === 0) {
  throw new Error(`no string constants found in ${src}`);
}

const lines = [
  "// AUTO-GENERATED by scripts/sync-launcher-protocol.mjs",
  "// Edit the source: src/shared/popup-launcher-protocol.ts",
  "// then rerun `npm run build` (or scripts/sync-launcher-protocol.mjs)",
  "// to regenerate this file.",
  "//",
  "// Constants are attached to window.OpenBiliClawLauncher so popup-",
  "// launcher.js can read them without an import (it's loaded as a",
  "// classic <script> tag, not a module).",
  "(function (root) {",
  "  \"use strict\";",
  "  root.OpenBiliClawLauncher = root.OpenBiliClawLauncher || {};",
  ...entries.map(([k, v]) => `  root.OpenBiliClawLauncher.${k} = ${JSON.stringify(v)};`),
  "})(typeof window !== \"undefined\" ? window : globalThis);",
  "",
];

await writeFile(dst, lines.join("\n"));
console.log(`✓ regenerated ${dst.replace(root + "/", "")} (${entries.length} constants)`);
